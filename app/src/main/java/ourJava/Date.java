public class Date
{
    int YEAR;
    int MONTH;
    int DAY;
    int MINUTES;
    int SECONDS;
    public Date(int year, int month, int day, int minutes, int seconds)
    {
        YEAR = year;
        MONTH = month;
        DAY = day;
        MINUTES = minutes;
        SECONDS = seconds;
    }
}
